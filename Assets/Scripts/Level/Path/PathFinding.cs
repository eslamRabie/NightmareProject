using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public abstract class PathFinding
{
    public abstract List<Node> FindPath(Node startNode, Node targetNode, out HashSet<Node> closedSet, in Grid grid);

    protected List<Node> RetracePath(Node startNode, Node endNode)
    {
        List<Node> path = new List<Node>();
        Node currentNode = endNode;

        while (currentNode != startNode)
        {
            path.Add(currentNode);
            currentNode = currentNode.parent;
        }
        path.Reverse();

        return path;
    }

    protected int GetDistance(Node nodeA, Node nodeB)
    {
        int dstX = Mathf.Abs(nodeA.gridX - nodeB.gridX);
        int dstY = Mathf.Abs(nodeA.gridY - nodeB.gridY);

        return dstX + dstY;
    }
}
